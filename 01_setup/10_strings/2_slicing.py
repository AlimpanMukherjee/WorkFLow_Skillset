name="Alimpan"

print(name[0:3])#goes from 0 to 2

print(name[0:-4])#same as [0:3]

print(name[0:8:2])#skip 1 character

print(name[0:8:3])#skip 2 characters

print(name[:4])#replace the first empty number with 0 , same as name[0:4]

print(name[1:])#repace the last empty number with n , same as name[1:n]