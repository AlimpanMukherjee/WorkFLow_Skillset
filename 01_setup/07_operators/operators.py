a=34
b=2
##arithmetic operator
print("a+b",a+b)
print("a/b",a/b)
print("a%b",a%b)
print("a//b",a//b)
print("a**b",a**b)

#conditional operator
print(a>4)
print(a<4)
print(a==34)

#logical operators
c=True
d=False
print(c and c)
print(c and d)
print(d and d)
print(c or c)
print(c or d)
print(d or d)
print(not(True))
print(not(False))

#Assignment operator
a-=9
print(a)

#membership operator
fruits=["apple" , "banana" , "mango"]
print("banana" in fruits)

